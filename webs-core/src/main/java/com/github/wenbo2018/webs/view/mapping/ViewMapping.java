public class ViewMapping {

    private int view;
    private String page;
    private int type;

    public int getView() {
        return view;
    }

    public void setView(int view) {
        this.view = view;
    }

    public String getPage() {
        return page;
    }

    public void setPage(String page) {
        this.page = page;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
